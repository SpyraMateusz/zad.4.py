def sort_integers(numbers):
    """
    Sorts a list of integers in ascending order.

    Parameters:
    - numbers (list): List of integers to be sorted.

    Returns:
    - list: Sorted list of integers.
    """
    sorted_numbers = sorted(numbers)
    return sorted_numbers
